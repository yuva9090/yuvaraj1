package com.loopingconcepts;

public class If_Else {
public static void main(String[] args) {
	
	for (int i = 10; i <=20; i++) {
		 
		
		if (i==15) {
			System.out.println(i);
			}
			
			else if (i==11) {
				System.out.println(i);
			}
		
			else {
				System.out.println("15 AND 11 IS NOT THERE");
			}
		
				
			}
		
		
		
	}
}
	
	
	
	


	
	
	

