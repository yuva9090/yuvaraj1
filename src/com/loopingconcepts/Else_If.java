package com.loopingconcepts;

public class Else_If {
	public static void main(String[] args) {
		
		for (int i = 44; i<=49; i++) {
		
		
		if (i==45) {
			System.out.println(i);
			
		}
		else if (i==47) {
			System.out.println(i)  ;
		}
		
		System.out.println("45 and 47 is not there");
		
		}
		}
		
		
	
	
	
	



}