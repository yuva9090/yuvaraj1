package com.Increment;

public class Increments {
	
	public static void main(String[] args) {
		
		
		//post increment  i++  ------ = i+1
		int i = 90;            // i = 91
		System.out.println(i++); 
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(i);
		
		
		// pre increment    ++i------- = 1+i
	  	
		int a = 4;
		System.out.println(++a);
		System.out.println(a);
	    System.out.println(++a);
	    System.out.println(++a);
		System.out.println(++a);
		
	}

}
